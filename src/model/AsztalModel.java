
package model;


public class AsztalModel {
    private String szin, etel;

    public AsztalModel(String szin, String etel) {
        this.szin = szin;
        this.etel = etel;
    }

    public String getSzin() {
        return szin;
    }

    public String getEtel() {
        return etel;
    }
    
    
}
